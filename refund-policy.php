         function createHoverAnimation(container, hoverElement) {
           let image = container.querySelector("img");
           let tl = gsap.timeline({ overwrite: true, paused: true });
           let animation = tl.fromTo(
             container,
             0.8,
             {
               yPercent: -100,
               autoAlpha: 0
             },
             {
               yPercent: 0,
               ease: Power2.out,
               autoAlpha: 1
             }
           );
           tl.from(image, 0.8, {
             yPercent: 100,
             scale: 1.3,
             delay: -0.8,
             ease: Power2.out
           });

           hoverElement.addEventListener("mouseenter", (e) => {
             animation.restart(true, false);
           });

           hoverElement.addEventListener("mouseleave", (e) => {
             animation.reverse();
           });
         }

